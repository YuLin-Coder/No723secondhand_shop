package edu.secondhand.entity;

public class Statics {
	
	private Integer counts;
	private String message;

	private Integer allTotals;
	private Integer allSums;
	private String allOrders;

	public Integer getAllTotals() {
		return allTotals;
	}

	public void setAllTotals(Integer allTotals) {
		this.allTotals = allTotals;
	}

	public Integer getAllSums() {
		return allSums;
	}

	public void setAllSums(Integer allSums) {
		this.allSums = allSums;
	}

	public String getAllOrders() {
		return allOrders;
	}

	public void setAllOrders(String allOrders) {
		this.allOrders = allOrders;
	}

	public Integer getCounts() {
		return counts;
	}
	public void setCounts(Integer counts) {
		this.counts = counts;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}

}
